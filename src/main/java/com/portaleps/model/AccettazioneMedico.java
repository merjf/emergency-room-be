package com.portaleps.model;

public class AccettazioneMedico {

    private String dataCarico;
    private String noteCarico;
    private String dataCessione;
    private String noteCessione;
    private String medico;

    public String getDataCarico() {
        return dataCarico;
    }

    public void setDataCarico(String dataCarico) {
        this.dataCarico = dataCarico;
    }

    public String getNoteCarico() {
        return noteCarico;
    }

    public void setNoteCarico(String noteCarico) {
        this.noteCarico = noteCarico;
    }

    public String getDataCessione() {
        return dataCessione;
    }

    public void setDataCessione(String dataCessione) {
        this.dataCessione = dataCessione;
    }

    public String getNoteCessione() {
        return noteCessione;
    }

    public void setNoteCessione(String noteCessione) {
        this.noteCessione = noteCessione;
    }

    public String getMedico() {
        return medico;
    }

    public void setMedico(String medico) {
        this.medico = medico;
    }
}
